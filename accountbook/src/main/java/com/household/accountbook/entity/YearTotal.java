package com.household.accountbook.entity;

public class YearTotal {

	private int accountId;
	private int totalYearSpending;
	private int totalYearIncome;
	private int yearBalance;
	public int getAccountId() {
		return accountId;
	}
	public void setAccountId(int accountId) {
		this.accountId = accountId;
	}
	public int getTotalYearSpending() {
		return totalYearSpending;
	}
	public void setTotalYearSpending(int totalYearSpending) {
		this.totalYearSpending = totalYearSpending;
	}
	public int getTotalYearIncome() {
		return totalYearIncome;
	}
	public void setTotalYearIncome(int totalYearIncome) {
		this.totalYearIncome = totalYearIncome;
	}
	public int getYearBalance() {
		return yearBalance;
	}
	public void setYearBalance(int yearBalance) {
		this.yearBalance = yearBalance;
	}
	
}
