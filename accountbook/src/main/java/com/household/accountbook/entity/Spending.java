package com.household.accountbook.entity;

public class Spending {

	private int id;
	private int accountId;
	private String spendingCategoryName;
	private int spendingAmount;
	private int spendingYear;
	private int spendingMonth;
	private int spendingDay;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAccountId() {
		return accountId;
	}
	public void setAccountId(int accountId) {
		this.accountId = accountId;
	}
	public String getSpendingCategoryName() {
		return spendingCategoryName;
	}
	public void setSpendingCategoryName(String spendingCategoryName) {
		this.spendingCategoryName = spendingCategoryName;
	}
	public int getSpendingAmount() {
		return spendingAmount;
	}
	public void setSpendingAmount(int spendingAmount) {
		this.spendingAmount = spendingAmount;
	}
	public int getSpendingYear() {
		return spendingYear;
	}
	public void setSpendingYear(int spendingYear) {
		this.spendingYear = spendingYear;
	}
	public int getSpendingMonth() {
		return spendingMonth;
	}
	public void setSpendingMonth(int spendingMonth) {
		this.spendingMonth = spendingMonth;
	}
	public int getSpendingDay() {
		return spendingDay;
	}
	public void setSpendingDay(int spendingDay) {
		this.spendingDay = spendingDay;
	}
	
	
	
	
	
	
}
